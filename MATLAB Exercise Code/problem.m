E=69*10.^9;
A=0.0004;

B = 6*(4/(E*A))
C = -16*(5/(E*A))
D = 10*(2/(E*A))
p=50;
for d=0:0.01:16 
        M = [1 1 1 -p; 0 10 16 -d*p; A B C 0];
        O = -1*rref(M);
        
        F1 = O(1,4);
        F2 = O(2,4);
        F3 = O(3,4);
        
        E1 = (F1*1000*100)/(E*A);
        E2 = (F2*1000*100)/(E*A);
        E3 = (F3*1000*100)/(E*A);
        plot(d, E1, 'kx');
        plot(d, E2, 'rx');
        plot(d, E3, 'bx');
